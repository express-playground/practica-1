// ==========================================
// REQUIRES
// ==========================================
const express = require('express')

// ==========================================
// INIT
// ==========================================
const app = express()
const routes = require('./routes')

// ==========================================
// SETTINGS
// ==========================================
app.set('port', 4500)

// ==========================================
// MIDDLEWARES
// ==========================================

// ==========================================
// DB CONECTIONS
// ==========================================

// ==========================================
// ROUTES
// ==========================================
app.use('/users', routes.userRoutes)

app.use('/', routes.appRoutes)

// ==========================================
// LISTENER
// ==========================================
app.listen(app.get('port'), () => {
  console.log('Server on port: ', app.get('port'))
})
